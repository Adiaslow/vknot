#!/usr/bin/env node
// ============================================================
//  sync-taxonomy.mjs — cache the arXiv category taxonomy.
//
//  Fetches https://arxiv.org/category_taxonomy, parses every
//  category's ID, human name, description, and parent group,
//  and writes them to src/data/arxiv-taxonomy.json — the cache
//  the shared <CategoryTag> component reads at build time.
//
//  This is a *triggered* sync, NOT a build hook: run it by hand
//  with `pnpm --filter @vknot/ui sync:taxonomy` (or `pnpm
//  sync:taxonomy` from this package) when you want to refresh
//  the cache. Ordinary builds stay offline and deterministic
//  by reading the committed JSON.
//
//  arXiv markup shape (stable for years):
//    <h2 class="accordion-head" id="accordion-head-grp_cs"> … Computer Science … </h2>
//      …
//      <div class="column is-one-fifth"><h4>cs.AI <span>(Artificial Intelligence)</span></h4></div>
//      <div class="column"><p>Covers all areas of AI …</p></div>
// ============================================================

import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_PATH = resolve(__dirname, '..', 'src', 'data', 'arxiv-taxonomy.json');
const SOURCE = 'https://arxiv.org/category_taxonomy';

/** Decode the handful of HTML entities arXiv descriptions use, then
 *  strip any stray inline tags and collapse whitespace. */
function cleanText(html) {
  return html
    .replace(/<[^>]+>/g, '')
    .replace(/&amp;/g, '&')
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&quot;/g, '"')
    .replace(/&#0?39;/g, "'")
    .replace(/&apos;/g, "'")
    .replace(/&nbsp;/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

// Each group is an accordion section; the group's display name is the
// text inside its toggle button. Capture id + name + the byte offset so
// we can attribute each category to the group it falls within.
const GROUP_RE =
  /<h2 class="accordion-head" id="accordion-head-grp_([\w-]+)">[\s\S]*?<button[^>]*>[\s\S]*?<\/span>\s*([^<]+?)\s*<\/button>/g;

// One category: the one-fifth column carries <h4>ID <span>(Name)</span></h4>,
// and the sibling column carries the <p>description</p> (occasionally absent).
const CATEGORY_RE =
  /<div class="column is-one-fifth">\s*<h4>\s*([\w.+-]+)\s*<span>\(([^)]*)\)<\/span>\s*<\/h4>\s*<\/div>\s*<div class="column">(?:\s*<p>([\s\S]*?)<\/p>)?/g;

async function main() {
  process.stdout.write(`Fetching ${SOURCE} …\n`);
  const res = await fetch(SOURCE, {
    headers: { 'User-Agent': 'vknot-taxonomy-sync/1.0 (+https://vknot.love)' },
  });
  if (!res.ok) {
    throw new Error(`arXiv responded ${res.status} ${res.statusText}`);
  }
  const html = await res.text();

  // Build an ordered list of [offset, groupId, groupName] boundaries.
  const groups = [];
  for (const m of html.matchAll(GROUP_RE)) {
    groups.push({ offset: m.index, id: m[1], name: cleanText(m[2]) });
  }
  if (groups.length === 0) {
    throw new Error('No accordion groups found — arXiv markup may have changed.');
  }
  const groupAt = (offset) => {
    let current = groups[0];
    for (const g of groups) {
      if (g.offset <= offset) current = g;
      else break;
    }
    return current;
  };

  const categories = [];
  const seen = new Set();
  for (const m of html.matchAll(CATEGORY_RE)) {
    const id = m[1].trim();
    if (seen.has(id)) continue;
    seen.add(id);
    const g = groupAt(m.index);
    categories.push({
      id,
      name: cleanText(m[2]),
      description: m[3] ? cleanText(m[3]) : '',
      group: g.name,
      groupId: g.id,
    });
  }
  if (categories.length === 0) {
    throw new Error('No categories parsed — arXiv markup may have changed.');
  }
  categories.sort((a, b) => a.id.localeCompare(b.id));

  const payload = {
    source: SOURCE,
    note: 'Generated by packages/ui/scripts/sync-taxonomy.mjs — run `pnpm sync:taxonomy` to refresh. Do not edit by hand.',
    count: categories.length,
    groups: groups.map((g) => ({ id: g.id, name: g.name })),
    categories,
  };

  mkdirSync(dirname(OUT_PATH), { recursive: true });
  writeFileSync(OUT_PATH, JSON.stringify(payload, null, 2) + '\n');
  process.stdout.write(
    `✓ Wrote ${categories.length} categories across ${groups.length} groups → ${OUT_PATH}\n`,
  );
}

main().catch((err) => {
  console.error(`✗ sync-taxonomy failed: ${err.message}`);
  process.exit(1);
});
